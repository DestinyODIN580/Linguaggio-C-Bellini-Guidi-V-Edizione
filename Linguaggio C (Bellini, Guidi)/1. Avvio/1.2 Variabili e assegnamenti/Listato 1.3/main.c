/* 1.3 - Uso di variabili */

/* Calcolo area di un rettangolo */

#include <stdio.h>

int main (int argc, const char *argv[])
{
    int base;
    int altezza;
    int area;

    base = 3;
    altezza = 7;
    area = base * altezza;

    
    printf ("Area >> %d\n", area);


    printf ("\n");
    return 0;
}
